            <p>In this opening paragraph of “The Call of Cthulhu,” first published in the pulp fiction magazine Weird Tales in 1928, H.P. Lovecraft set out a view of things that animates pretty well everything he wrote thereafter: The human mind is an accident in the universe, which is indifferent to the welfare of the species. We can have no view of the scheme of things or our place in it, because there may be no such scheme. The final result of scientific inquiry could well be that the universe is a lawless chaos. Sometimes called “weird realism,” it is a disturbing vision with which Lovecraft would struggle throughout his life.</p>
